package typeconversion;

public class primitivetypeconvo {
    public static void main(String[] args) {
        char ch = 'A';
        int a = ch;
        System.out.println(ch);
        System.out.println(a);


        int x = 20;
        int b = 30;
        double c = x+b;
        System.out.println(c);

        int h = 10;
        double i = 10;
        double k = (h + i);
        System.out.println(h+" + "+i+" = "+k);
    }
}
