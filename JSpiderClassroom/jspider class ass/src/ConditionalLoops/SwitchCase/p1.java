package ConditionalLoops.SwitchCase;

public class p1 {
    public static void main(String[] args) {
        int a = 1;

        switch (a){
            case 1:
                System.out.println("Hi...");
                break;
            case 2:
                System.out.println("Hello...");
                break;
            case 3:
                System.out.println("Bye....");
                break;
        }
    }
}
