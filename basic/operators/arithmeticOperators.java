package operators;

public class arithmeticOperators {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;

        //1. Addition
        System.out.println("a + b = "+ (a+b));
        //2. Substraction
        System.out.println("a - b = "+ (a-b));
        //3. multiplication
        System.out.println("a * b = "+ (a*b));
        //4. Divide
        System.out.println("a / b = "+ (a/b));
        //5. modulous
        System.out.println("a % b = "+ (a%b));
    }
}
